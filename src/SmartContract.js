const contractAddress = "0x06F979F9074c61330a1A0dCb93e80E9918CdFeB6"; // Replace with the actual contract address

const SmartContract = () => {
  const [web3, setWeb3] = useState(null);
  const [accounts, setAccounts] = useState([]);
  const [stakeAmount, setStakeAmount] = useState("");
  const [contract, setContract] = useState(null);

  useEffect(() => {
    initializeWeb3();
  }, []);

  const initializeWeb3 = useCallback(async () => {
    if (window.ethereum) {
      const _web3 = new Web3(window.ethereum);
      setContract(new _web3.eth.Contract(contractAbi, contractAddress));
      try {
        await window.ethereum.request({
          method: "wallet_switchEthereumChain",
          params: [{ chainId: "0x13881" }],
        });
        await window.ethereum.enable();
        setWeb3(_web3);
        const _accounts = await _web3.eth.getAccounts();
        setAccounts(_accounts);
      } catch (error) {
        console.error("Error connecting to MetaMask");
      }
    } else {
      alert("MetaMask not found");
    }
  }, []);

  const handleStake = useCallback(async () => {
    if (web3 && accounts.length > 0) {
      try {
        await contract.methods
          .stake(parseInt(parseFloat(stakeAmount) * 10 ** 18).toFixed())
          .send({ from: accounts[0] });
        alert("Stake successful");
      } catch (error) {
        console.error("Error staking tokens", error);
      }
    }
  }, [accounts, stakeAmount, contract, web3]);

  const handleUnstake = useCallback(async () => {
    if (web3 && accounts.length > 0) {
      try {
        await contract.methods.unstake().send({ from: accounts[0] });
        alert("UnStake successful");
      } catch (error) {
        console.error("Error staking tokens", error);
      }
    }
  }, [accounts, contract]);

  return (
    <div>
      <h1>Staking App</h1>
      <h2>Connected Account: {accounts[0]}</h2>
      <input
        type="number"
        value={stakeAmount}
        onChange={(e) => setStakeAmount(e.target.value)}
      />
      <button onClick={handleStake}>Stake</button>
      <br />
      <br />
      <button onClick={handleUnstake}>UnStake</button>
    </div>
  );
};

export default SmartContract;
